from __future__ import division


num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

Adding_numbers = num1 + num2
Subtracting_numbers = num1 - num2
Multiplication_numbers = num1 * num2
division_numbers = num1 / num2
modulus_number = num1 % num2

print(f"Sum of the two numbers is {Adding_numbers}")
print(f"Subtraction the two numbers is {Subtracting_numbers}")
print(f"Multiplication of the two numbers is {Multiplication_numbers}")
print(f"Division of the two numbers is {division_numbers}")
print(f"Modulus of the two numbers is {modulus_number}")